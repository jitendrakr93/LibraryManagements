package com.library.LibraryManagements.subadminvo;

public class SubAdmin {
	private long superAdminId;
	private long subAdminId;
	private String subAdminName;
	private String subAdminEmail;
	private long subAdminMobileNo;
	private String subAdminPassword;
	public long getSuperAdminId() {
		return superAdminId;
	}
	public void setSuperAdminId(long superAdminId) {
		this.superAdminId = superAdminId;
	}
	public long getSubAdminId() {
		return subAdminId;
	}
	public void setSubAdminId(long subAdminId) {
		this.subAdminId = subAdminId;
	}
	public String getSubAdminName() {
		return subAdminName;
	}
	public void setSubAdminName(String subAdminName) {
		this.subAdminName = subAdminName;
	}
	public String getSubAdminEmail() {
		return subAdminEmail;
	}
	public void setSubAdminEmail(String subAdminEmail) {
		this.subAdminEmail = subAdminEmail;
	}
	public long getSubAdminMobileNo() {
		return subAdminMobileNo;
	}
	public void setSubAdminMobileNo(long subAdminMobileNo) {
		this.subAdminMobileNo = subAdminMobileNo;
	}
	public String getSubAdminPassword() {
		return subAdminPassword;
	}
	public void setSubAdminPassword(String subAdminPassword) {
		this.subAdminPassword = subAdminPassword;
	}
	
	

}
